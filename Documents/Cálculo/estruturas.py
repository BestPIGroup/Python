# Lista: É mutável, é ordenado, é indexado de 0...n
lista=[10,20,30]
# Tupla: É imutável, é ordenado, é indexado de 0...n
tupla=(10,20,30)
# Dicionário: É mutável, é ordenado a partir da v3.7.x, é indexado a partir da chave
dicionario={"num01":10,"num02":20,"num03":30}
# Matriz= É mutável, é ordenado, é indexado de 0...n. É uma lista de listas.
matriz=[[10,20,30]]

print(type(lista))
print(type(tupla))
print(type(dicionario))
print(type(matriz))